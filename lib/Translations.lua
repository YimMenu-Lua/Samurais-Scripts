local logMsg = true
--[[

Translates text to the user's language.

If the label to translate is missing or the language is invalid then it defaults to English (US).
]]
---@param g string
function translateLabel(g)
    ---@type string
    local retStr
    if Labels[g] then
        for _, v in pairs(Labels[g]) do
            if LANG == v.iso then
                retStr = v.text
                break
            end
        end
        if retStr == nil or retStr == "" then
            if logMsg then
                gui.show_warning("Samurai's Scripts", "Unsupported language or missing label(s) detected! Defaulting to English.")
                log.warning("Unsupported language or missing label(s) detected! Defaulting to English.")
            end
            retStr = Labels[g][1].text
            logMsg = false
        end
    else
        retStr = tostring(g) .. " [LABEL NOT FOUND!]"
    end
    return retStr
end
